class Food {
  final int? id;
  final String? name;
  final String? description;
  final String? image;
  final double? price;
  final double? discount;
  final double? ratings;
  final String? calories;
  final String? time;

  Food({
      required this.id,
      required this.name,
      required this.description,
      required this.image,
      required this.price,
      required this.discount,
      required this.ratings,
      required this.calories,
      required this.time,
      });
}
